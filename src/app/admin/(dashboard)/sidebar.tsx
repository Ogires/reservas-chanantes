export function Sidebar({ tenant, setupStatus }: { tenant: Tenant; setupStatus: SetupStatus }) {
  const pathname = usePathname()

  function showBadge(badgeKey: 'hasServices' | 'hasSchedule' | null): boolean {
    if (!badgeKey) return false
    return !setupStatus[badgeKey]
  }

  return (
    <aside className="flex h-screen w-56 flex-col border-r border-slate-200 bg-white p-4">
      <div className="mb-6">
        <h2 className="text-lg font-bold text-slate-900 truncate">{tenant.name}</h2>
        <p className="text-xs text-slate-500">/{tenant.slug}</p>
      </div>

      <nav className="flex flex-1 flex-col gap-1">
        {navItems.map((item) => {
          const isActive = pathname.startsWith(item.href)
          return (
            <Link
              key={item.href}
              href={item.href}
              className={`flex items-center gap-3 rounded-lg px-3 py-2 text-sm transition-colors ${
                isActive
                  ? 'bg-indigo-50 text-indigo-700 font-medium'
                  : 'text-slate-600 hover:bg-slate-100 hover:text-slate-900'
              }`}
            >
              {item.icon}
              {item.label}
              {showBadge(item.badgeKey) && (
                <span className="ml-auto h-2 w-2 rounded-full bg-amber-400" />
              )}
            </Link>
          )
        })}
        <Link
          href={`/${tenant.slug}`}
          target="_blank"
          className="flex items-center gap-3 rounded-lg px-3 py-2 text-sm text-slate-600 hover:bg-slate-100 hover:text-slate-900 transition-colors"
        >
          <svg className="h-5 w-5" fill="none" viewBox="0 0 24 24" strokeWidth={1.5} stroke="currentColor">
            <path strokeLinecap="round" strokeLinejoin="round" d="M13.5 6H5.25A2.25 2.25 0 0 0 3 8.25v10.5A2.25 2.25 0 0 0 5.25 21h10.5A2.25 2.25 0 0 0 18 18.75V10.5m-10.5 6L21 3m0 0h-5.25M21 3v5.25" />
          </svg>
          View public page
        </Link>
      </nav>

      <LogoutButton />
    </aside>
  )
}
